package day1;

/**
 *  备注 测试git账号
 *  常量是不能改变的数值
 *  变量就如同数字中的未知数
 *  格式就是数据类型 变量名 = 初始化值;
 */
public class day11 {
    public static void main(String[] args) {
        System.out.println("丢你吗你麻痹");
        System.out.println("傻嗨");
        byte b=3;
        short s=4;
        int i= 7;
        float z=2.3f;
        char c=1;
        boolean bo=true;
    }
}
